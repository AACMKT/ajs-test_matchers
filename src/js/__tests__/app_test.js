import healthStatusSorter from '../app';

test('character sorting by health', () => {
  const testArray = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const sortedArray = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  expect(healthStatusSorter(testArray)).toEqual(sortedArray);
});
